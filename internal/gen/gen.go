package gen

import (
	"fmt"
	"io/ioutil"
	"path/filepath"
	"sort"
	"strings"

	"github.com/desertbit/orbit/internal/parse"
	"github.com/desertbit/orbit/internal/utils"
)

const (
	OrbitSuffix = ".orbit"

	filePerm  = 0644
	genSuffix = "_gen.go"
	recv      = "v1"
)

func Generate(filePath string) (err error) {
	// Read the file.
	data, err := ioutil.ReadFile(filePath)
	if err != nil {
		return
	}

	// Parse the file data.
	errs, services, types, err := parse.Parse(string(data))
	if err != nil {
		err = fmt.Errorf("could not parse %s: %v", filePath, err)
		return
	}

	// Create generator.
	g := newGenerator()

	// Add imports that we always need.
	g.imps.Add("github.com/desertbit/orbit/internal/control")

	// Sort the types in alphabetical order.
	sort.Slice(types, func(i, j int) bool {
		return types[i].Name < types[j].Name
	})

	// Generate the errors.
	g.genErrors(errs)

	// Generate the type definitions.
	g.genStructTypes(types)

	// Generate the service definitions.
	g.genServices(services, errs)

	// Write the preamble.
	var code string
	dir := filepath.Dir(filePath)
	code += fmt.Sprintf("/* code generated by orbit */\npackage %s\n\n", filepath.Base(dir))

	// Write the imports.
	if len(g.imps) > 0 {
		code += "import (\n"
		for imp := range g.imps {
			code += "\t\"" + imp + "\"\n"
		}
		code += ")\n\n"
	}

	// Write the contents to the file.
	return ioutil.WriteFile(
		filepath.Join(dir, strings.TrimSuffix(filepath.Base(filePath), OrbitSuffix)+genSuffix),
		[]byte(code+g.s.String()),
		filePerm,
	)
}

type generator struct {
	s           strings.Builder
	ind         int
	writeIndent bool
	imps        imports
}

func newGenerator() *generator {
	return &generator{
		imps: newImports(),
	}
}

func (g *generator) genErrors(errs []*parse.Error) {
	g.writeLn("//##############//")
	g.writeLn("//### Errors ###//")
	g.writeLn("//##############//")
	g.writeLn("")

	if len(errs) == 0 {
		return
	}

	// Add import.
	g.imps.Add("errors")

	// Open const block.
	g.writeLn("const (")
	g.indent(func() {
		// Write error codes.
		for _, e := range errs {
			g.writeLn("ErrCode%s = %d", e.Name, e.ID)
		}
	})
	// Close const block.
	g.writeLn(")")

	// Open var block.
	g.writeLn("var (")
	g.indent(func() {
		// Write standard error variables along with the orbit control.ErrorCode ones.
		for _, e := range errs {
			g.writeLn("Err%s = errors.New(\"%s\")", e.Name, strExplode(e.Name))
			g.writeLn("orbitErr%s = control.Err(", e.Name)
			g.indent(func() {
				g.writeLn("Err%s,", e.Name)
				g.writeLn("Err%s.Error(),", e.Name)
				g.writeLn("ErrCode%s,", e.Name)
			})
			g.writeLn(")")
			g.writeLn("")
		}
	})
	// Close var block.
	g.writeLn(")")
	g.writeLn("")
}

func (g *generator) genStructTypes(ts []*parse.StructType) {
	g.writeLn("//#############//")
	g.writeLn("//### Types ###//")
	g.writeLn("//#############//")
	g.writeLn("")

	for _, t := range ts {
		// Sort its fields in alphabetical order.
		sort.Slice(t.Fields, func(i, j int) bool {
			return t.Fields[i].Name < t.Fields[j].Name
		})

		g.writeLn("type %s struct {", t.Name)
		g.indent(func() {
			for _, f := range t.Fields {
				g.write("%s ", f.Name)
				g.genType(f.Type)
				g.writeLn("")
			}
		})
		g.writeLn("}")
		g.writeLn("")
	}
}

func (g *generator) genType(t parse.Type) {
	switch v := t.(type) {
	case *parse.StructType:
		// Structs just require a reference.
		g.write("*%s", v.Name)
	case *parse.MapType:
		g.write("map[")
		// Generate Key type.
		g.genType(v.Key)
		g.write("]")
		// Generate Value type.
		g.genType(v.Value)
	case *parse.ArrType:
		g.write("[]")
		// Generate Elem type.
		g.genType(v.ElemType)
	case *parse.BaseType:
		dt := v.DataType()

		// Check, if an import is needed.
		if dt == parse.TypeTime {
			g.imps.Add("time")
			g.write("time.Time")
		} else {
			g.write(dt)
		}
	}
}

func (g *generator) genServices(services []*parse.Service, errs []*parse.Error) {
	g.writeLn("//################//")
	g.writeLn("//### Services ###//")
	g.writeLn("//################//")
	g.writeLn("")

	for _, srvc := range services {
		g.genService(srvc, errs)
	}
}

func (g *generator) genService(srvc *parse.Service, errs []*parse.Error) {
	g.writeLn("// %s  ---------------------", srvc.Name)

	var (
		calls      = make([]*parse.Call, 0)
		revCalls   = make([]*parse.Call, 0)
		streams    = make([]*parse.Stream, 0)
		revStreams = make([]*parse.Stream, 0)
	)

	// Sort the entries into the respective categories.
	// Also create the call ids.
	g.writeLn("const (")
	g.indent(func() {
		for _, e := range srvc.Entries {
			g.writeLn("%s = \"%s\"", e.Name(), e.Name())
			switch v := e.(type) {
			case *parse.Call:
				if v.Rev() {
					revCalls = append(revCalls, v)
				} else {
					calls = append(calls, v)
				}
			case *parse.Stream:
				if v.Rev() {
					revStreams = append(revStreams, v)
				} else {
					streams = append(streams, v)
				}
			}
		}
	})
	g.writeLn(")")
	g.writeLn("")

	// Imports!
	if len(streams) > 0 || len(revStreams) > 0 {
		g.imps.Add("net")
	}

	// Create the interfaces.
	g.genServiceInterface(srvc.Name+"ConsumerCaller", calls, streams)
	g.genServiceInterface(srvc.Name+"ConsumerHandler", revCalls, revStreams)
	g.genServiceInterface(srvc.Name+"ProviderCaller", revCalls, revStreams)
	g.genServiceInterface(srvc.Name+"ProviderHandler", calls, streams)

	// Create the private structs implementing the caller interfaces and providing the orbit handlers.
	g.genServiceStruct(srvc.Name+"Consumer", calls, streams)
	g.genServiceStruct(srvc.Name+"Provider", revCalls, revStreams)

	g.writeLn("// ---------------------\n")
	g.writeLn("")
	return
}

func (g *generator) genServiceInterface(name string, calls []*parse.Call, streams []*parse.Stream) {
	g.writeLn("type %s interface {", name)
	g.indent(func() {
		g.writeLn("// Calls")
		for _, c := range calls {
			g.genServiceCallSignature(c.Name(), c.Args, c.Ret)
			g.writeLn("")
		}
		g.writeLn("// Streams")
		for _, st := range streams {
			g.writeLn("%s(conn net.Conn) (err error)", st.Name())
		}
	})
	g.writeLn("}")
	g.write("")
}

func (g *generator) genServiceStruct(name string, calls []*parse.Call, streams []*parse.Stream) {
	// One service has one control for all normal calls.
	// But every async call gets its own control.
	numControls := 1
	for _, c := range calls {
		if c.Async {
			numControls++
		}
	}

	// Write struct.
	strName := utils.ToLowerFirst(name)
	g.writeLn("type %s struct {", strName)

	g.indent(func() {
		g.writeLn("h %sHandler", name)
		g.writeLn("ctrls [%d]*control.Control", numControls)
	})

	g.writeLn("}")
	g.writeLn("")

	asyncCount := 1
	for _, c := range calls {
		ctrlIndex := 0

		if c.Async {
			ctrlIndex = asyncCount
			asyncCount++
		}

		g.genServiceCall(c, strName, ctrlIndex)
	}
}

func (g *generator) genServiceCall(c *parse.Call, structName string, ctrlIndex int) {
	// Method declaration.
	g.write("func (%s *%s) ", recv, structName)
	g.genServiceCallSignature(c.Name(), c.Args, c.Ret)
	g.writeLn(" {")
	g.indent(func() {
		// Method body.
		// First, make the call.
		if c.Ret != nil {
			g.write("ctx, err := ")
		} else {
			g.write("_, err = ")
		}
		g.write("%s.ctrls[%d].Call(%s, ", recv, ctrlIndex, c.Name())
		if c.Args != nil {
			g.writeLn("args)")
		} else {
			g.writeLn("nil)")
		}

		// Check error.
		g.writeErrNilCheck(nil) // todo;

		// If return arguments are expected, decode them.
		if c.Ret != nil {
			g.writeLn("err = ctx.Decode(ret)")
			g.writeErrNilCheck(nil) // todo;
		}

		// Return.
		g.writeLn("return")
	})
	g.writeLn("}")
	g.writeLn("")
}

func (g *generator) genServiceCallOrbitHandler(c *parse.Call, structName string) {
	cname := utils.ToLowerFirst(c.Name())

	// Method declaration.
	g.writeLn("func (%s *%s) %s(ctx *control.Context) (v interface{}, err error) {", recv, structName, cname)
	g.indent(func() {
		// Method body.
		// Parse the args.
		if c.Args != nil {
			g.writeLn("var args %s", c.Args.Type.Name)
			g.writeLn("err = ctx.Decode(args)")
			g.writeErrNilCheck(nil) // todo;
		}

		// Call the handler.
		g.writeLn("ret, err := %s.h.%s(args)", recv, cname)
		g.writeErrNilCheck(nil) // todo;

		// Return.
		g.writeLn("return")
	})
	g.writeLn("}")
	g.writeLn("")
}

func (g *generator) genServiceCallSignature(name string, args, ret *parse.EntryParam) {
	g.write("%s(", name)
	if args != nil {
		g.write("args *%s", args.Type.Name)
	}
	g.write(") (")
	if ret != nil {
		g.write("ret *%s, ", ret.Type.Name)
	}
	g.write("err error)")
}

func (g *generator) writeErrNilCheck(errs []*parse.Error) {
	g.writeLn("if err != nil {")
	g.indent(func() {
		// Check, if an control.ErrorCode has been returned.
		if errs != nil {
			g.imps.Add("errors")

			g.writeLn("var cErr *control.ErrorCode")
			g.writeLn("if errors.As(err, &cErr) {")

			g.indent(func() {
				g.writeLn("switch cErr.Code {")
				// todo:
			})
		}
		g.writeLn("return")
	})
	g.writeLn("}")
}

func (g *generator) indent(f func()) {
	g.ind++
	f()
	g.ind--
}

func (g *generator) writeLn(format string, a ...interface{}) {
	g.write(format, a...)
	g.s.WriteString("\n")
	g.writeIndent = true
}

func (g *generator) write(format string, a ...interface{}) {
	if g.writeIndent {
		for i := 0; i < g.ind; i++ {
			g.s.WriteString("\t")
		}
		g.writeIndent = false
	}

	if len(a) == 0 {
		g.s.WriteString(format)
		return
	}

	g.s.WriteString(fmt.Sprintf(format, a...))
}
